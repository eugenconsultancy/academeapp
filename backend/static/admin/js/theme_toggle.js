// ============================================
// ACADEME ADMIN – THEME TOGGLE & DASHBOARD ENHANCEMENTS
// ============================================

(function () {
    // ---------- THEME TOGGLE ----------
    const toggleBtn = document.createElement('button');
    toggleBtn.className = 'theme-toggle-btn';
    toggleBtn.innerHTML = '<i class="fas fa-moon"></i>';
    toggleBtn.setAttribute('aria-label', 'Toggle Dark/Light Mode');

    const navbar = document.querySelector('.navbar-nav.ml-auto');
    if (navbar) {
        const li = document.createElement('li');
        li.className = 'nav-item';
        li.appendChild(toggleBtn);
        navbar.appendChild(li);
    }

    const userPrefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
    let currentTheme = localStorage.getItem('academe_theme') || (userPrefersDark ? 'dark' : 'light');

    function setTheme(theme) {
        const body = document.body;
        if (theme === 'light') {
            body.classList.add('light-mode');
            body.classList.remove('dark-mode');
            toggleBtn.innerHTML = '<i class="fas fa-moon"></i>';
        } else {
            body.classList.add('dark-mode');
            body.classList.remove('light-mode');
            toggleBtn.innerHTML = '<i class="fas fa-sun"></i>';
        }
        localStorage.setItem('academe_theme', theme);
    }
    setTheme(currentTheme);
    toggleBtn.addEventListener('click', () => {
        const newTheme = currentTheme === 'dark' ? 'light' : 'dark';
        setTheme(newTheme);
        currentTheme = newTheme;
    });

    // ---------- DASHBOARD: CONVERT MODEL LIST TO FEATURE TILES ----------
    function enhanceModelList() {
        const modelListContainer = document.querySelector('.model-list-widget');
        if (!modelListContainer) return;

        // Find the list of model links (generated by Jazzmin)
        const modelLinks = modelListContainer.querySelectorAll('a');
        if (modelLinks.length === 0) return;

        // Create grid container
        const grid = document.createElement('div');
        grid.className = 'model-tile-grid';
        modelListContainer.innerHTML = '';
        modelListContainer.appendChild(grid);

        // Define metadata for each model (descriptors and actions)
        const modelMetadata = {
            'User': { desc: 'Manage students, staff, and roles', actions: ['Add', 'List', 'Export'] },
            'FoundItem': { desc: 'Lost & found items, claims, payments', actions: ['Add', 'List', 'Claims'] },
            'Announcement': { desc: 'Campus announcements & requests', actions: ['Add', 'List', 'Reports'] },
            'Opportunity': { desc: 'Jobs, internships, and events', actions: ['Add', 'List', 'Likes'] },
            'ClassGroup': { desc: 'Class groups, timetable, attendance', actions: ['Add', 'List', 'Timetable'] },
            'SupportTicket': { desc: 'User support tickets & responses', actions: ['List', 'Open', 'Resolved'] },
        };

        modelLinks.forEach(link => {
            const modelName = link.innerText.trim();
            const href = link.getAttribute('href');
            const meta = modelMetadata[modelName] || { desc: 'Manage records', actions: ['Add', 'List'] };

            const tile = document.createElement('div');
            tile.className = 'model-tile';

            // Icon based on model (fallback)
            let iconHtml = '<i class="fas fa-cube"></i>';
            if (modelName === 'User') iconHtml = '<i class="fas fa-users"></i>';
            if (modelName === 'FoundItem') iconHtml = '<i class="fas fa-box-open"></i>';
            if (modelName === 'Announcement') iconHtml = '<i class="fas fa-bullhorn"></i>';
            if (modelName === 'Opportunity') iconHtml = '<i class="fas fa-briefcase"></i>';
            if (modelName === 'ClassGroup') iconHtml = '<i class="fas fa-layer-group"></i>';
            if (modelName === 'SupportTicket') iconHtml = '<i class="fas fa-ticket-alt"></i>';

            tile.innerHTML = `
                <div class="tile-header">
                    <div class="tile-icon">${iconHtml}</div>
                    <div class="tile-title">${modelName}</div>
                </div>
                <div class="tile-desc">${meta.desc}</div>
                <div class="tile-actions">
                    ${meta.actions.map(action => {
                let url = href;
                if (action === 'Add') url = href.replace(/\/$/, '') + '/add/';
                else if (action === 'List') url = href;
                else if (action === 'Claims') url = href + '?claim__isnull=false';
                else if (action === 'Reports') url = href + '?report__isnull=false';
                else if (action === 'Likes') url = href + '?like__isnull=false';
                else if (action === 'Timetable') url = '/admin/classes/timetableentry/';
                else if (action === 'Open') url = href + '?status=open';
                else if (action === 'Resolved') url = href + '?status=resolved';
                else url = href;
                return `<a href="${url}" class="btn btn-sm btn-secondary">${action}</a>`;
            }).join('')}
                </div>
            `;
            grid.appendChild(tile);
        });
    }

    // ---------- RECENT ACTIVITY: ENSURE SCROLLABLE CONTAINER ----------
    function enhanceRecentActivity() {
        const activityWidget = document.querySelector('.recent-actions-widget');
        if (!activityWidget) return;
        // Wrap inner content in scrollable div
        const originalContent = activityWidget.innerHTML;
        activityWidget.innerHTML = `<div class="recent-activity-container">${originalContent}</div>`;
        // Style each activity line as a mini-card
        const items = activityWidget.querySelectorAll('.recent-actions-list li, .recent-actions-list tr');
        items.forEach(item => {
            item.classList.add('activity-item');
            // Add icon if missing
            if (!item.querySelector('.activity-icon')) {
                const iconSpan = document.createElement('span');
                iconSpan.className = 'activity-icon';
                iconSpan.innerHTML = '<i class="fas fa-history"></i>';
                item.prepend(iconSpan);
            }
        });
    }

    // Run after DOM is ready and after Jazzmin loads (setTimeout for safety)
    window.addEventListener('load', () => {
        setTimeout(() => {
            enhanceModelList();
            enhanceRecentActivity();
        }, 300);
    });
})();